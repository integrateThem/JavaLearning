package cumaDers1.hesapMakinesi;

public class HesapMakinesi {
    public int hesapla(int a,int b){
        int toplam=a+b;
        return toplam;
    }
    public int hesapla(int a,int b,int c) {
        int toplam=a+b+c;
        return toplam;
    }
    public float hesapla(float a,float b){
        float toplam=a+b;
        return toplam;
    }
    public float hesapla(float a,float b,float c,float d){
        float toplam=a+b+c+d;
        return toplam;
    }
}
